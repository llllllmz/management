package com.lmz.domain;

public class Goods {
    private int g_id;
    private String g_name;
    private double g_price;
    private String g_firm;
    private int g_num;
    private String g_img;


    public String getG_img() {
        return g_img;
    }

    public void setG_img(String g_img) {
        this.g_img = g_img;
    }

    public int getG_id() {
        return g_id;
    }

    public void setG_id(int g_id) {
        this.g_id = g_id;
    }

    public String getG_name() {
        return g_name;
    }

    public void setG_name(String g_name) {
        this.g_name = g_name;
    }

    public double getG_price() {
        return g_price;
    }

    public void setG_price(double g_price) {
        this.g_price = g_price;
    }

    public String getG_firm() {
        return g_firm;
    }

    public void setG_firm(String g_firm) {
        this.g_firm = g_firm;
    }

    public int getG_num() {
        return g_num;
    }

    public void setG_num(int g_num) {
        this.g_num = g_num;
    }
}
